package operators;

public class Incr_Ex
{
    public static void main(String[] args)
    {
        // Increment operator..,
        int a = 4;
        System.out.println(a++);    // post increment
        System.out.println(++a);    // pre increment

        System.out.println(a--);    // post decrement
        System.out.println(--a);    // pre decrement

    }
}
